/// Defines standard spacing constants for consistent layout across the app.
class AppSpacing {
  /// Extra extra small spacing value (2.0).
  static const double xxs = 2.0;

  /// Extra small spacing value (4.0).
  static const double xs = 4.0;

  /// Small spacing value (8.0).
  static const double s = 8.0;

  /// Medium spacing value (12.0).
  static const double m = 12.0;

  /// Large spacing value (16.0). Often used for default padding.
  static const double l = 16.0;

  /// Extra large spacing value (24.0).
  static const double xl = 24.0;

  /// Extra extra large spacing value (32.0).
  static const double xxl = 32.0;

  // Private constructor to prevent instantiation
  AppSpacing._();
}
